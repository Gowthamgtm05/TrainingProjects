package org.programs;

public class NumberOfOddandEven {

	public static void main(String[] args) {

		int even=0,odd=0;
		
		for (int i = 0; i <= 10; i++) {
			
			if (i%2==0) {
				even++;
			}
			else {
				odd++;
			}
		}
		System.out.println("odd" +odd);
		System.out.println("even" +even);
		
		
		
		
		
		
		
		
		
		
		
	}
	
}
